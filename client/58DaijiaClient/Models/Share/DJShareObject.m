#import "DJShareObject.h"

@implementation DJShareObject

@synthesize title, description , image , url ;

-(void ) deserialize:(NSMutableDictionary *) dict {
    
    self.title = [dict objectForKey:@"title" ] ;
    self.description  = [dict objectForKey:@"desc" ] ;
    self.image = [dict objectForKey:@"thumbnail" ] ;
    self.url = [dict objectForKey:@"url" ] ;

}

@end
